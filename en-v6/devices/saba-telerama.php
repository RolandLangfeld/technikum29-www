<p>
         The CRT (6&nbsp;cm diameter) is partly visible in the pice. The light
         coming from this tube caroms on the concave mirror (in the picture laying on
         the floor). Afterwards it is adjusted by an special lens (on the right of the
         box) and leaves the box, arriving at the projection screen.
         <br />During this long way the original picture which is about
         4,5cm&nbsp;x&nbsp;3,5cm big is scaled up to 123cm&nbsp;x&nbsp;97,5cm on the
         projection screen. Consequently, the luminosity is decreasing enormously.
         If you compute the final fraction of the original luminosity, you'll
         understand, why this projection TV had to be set in a totally shaded room.</p>
