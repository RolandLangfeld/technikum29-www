<h2>LGP 30 von Schoppe & Faeser</h2>

<p>Ein interessanter Neuzugang im September 2015:<br><br>
Dieser für damalige Verhältnisse sehr kleine Rechner kam 1958 auf den Europäischen Markt. Er wurde von der deutschen Firma Schoppe & Faeser in Lizenz von Librascope (USA) gebaut. In den USA wurde das Modell ab 1956 verkauft. Gerade mal ca. 40 Stück wurden von dem Röhrencomputer der 1. Generation in Europa gebaut. Er war als wissenschaftlicher Rechner mit Trommelspeicher konzipiert und arbeitet mit nur 115 Elektronenröhren.<br>
Zum Speichern von Programmen und Daten werden 1"-Lochstreifen verwendet, die ein Schnell-Leser bzw. -Stanzer verarbeitet. Als "Drucker" (besser Ausgabegerät) dient ein "Flexowriter", im Prinzip eine elektrische Schreibmaschine mit Lochstreifenzusatz.</p>

