	<p>Der Neuzugang (12.2017) zeigt die etwas verkleinerte Ausführung des 2200 aus dem Jahre 1974/75. "S" steht wohl für "small". Das zusätzliche Kassettenlaufwerk 2217 hat eine Kapazität von 1,7 KB pro Meter Band und lässt einen Transfer von 326 Zeichen/Sekunde zu. Es ist mit drei Motoren ausgestattet und kann in beide Laufrichtungen lesen/schreiben. Dabei werden immer Blöcke mit 256 Bytes gelesen/geschrieben. Dieses langsame Speichermedium war damals halt konkurrenzlos preiswert.<br>
	Interessant ist auch die dazugehörige Kugelkopfmaschine (2202) als Ausgabegerät. Ausgestattet mit Schrittschaltmotoren kann die Maschine auch plotten. Das Modell ist identisch mit dem des <a class="go" href="/de/rechnertechnik/ic-technik.php#wang700" target="_blank">WANG 700 Rechners.</a></p>
