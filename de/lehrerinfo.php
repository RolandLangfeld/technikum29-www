	<h2>Didaktische Konzeption, Informationen für Lehrer</h2>
	<p>
		Prinzipiell bieten wir je einen Workshop mit dem Themenbereich "<b>Computer History</b>" oder "<b>Kommunikationstechnik</b>" an. Diese eignen sich für technisch interessierte Gruppen und Menschen, die einfach nur neugierig sind und sich in der Kulturgeschichte der intelligentesten und wichtigsten Werkzeuge, wie Computer und Kommunikationstechniken, weiter bilden möchten. Sie müssen sich zwischen diesen Themen entscheiden, da beide Gebiete in einer Führung mental überfordern würden und zeitlich nicht realisierbar sind.<br>
		Lassen Sie sich nicht durch die speziellen Themen (rechte Navigation) einschüchtern! Wir gestalten Führungen und Workshop´s so, dass auch relative Laien Spaß daran haben. Sie werden nicht durch Fachwissen erschlagen.
		
		<div class="box bordered nomargin-bottom">
	<p class="bildtext"><b><big>Geburtstags-Event für 11 bis 13-Jährige</big></b> <br><br>
	Das technikum29 bietet für Kinder und Jugendliche im Alter von 11 bis 13 Jahren einen Geburtstags-Event der besonderen Art an: <br>
	Zunächst erfolgt eine kurze, altersgemäße Führung durch die Epochen der Computerwelten. Hier werden die Kids immer mit einbezogen.<br>
	Nach einer kurzen Pause experimentieren die Kids in kleinen Gruppen an wirklich interessanten Projekten, die so viel Spaß machen, dass das Ende der "Feier" schwer fällt. 
	Eine abwechslungsreiche und intelligente Alternative zu den üblichen Geburtstagspartys.</p>
</div>
		
		
		
		
   <br><br>
		Für interessierte Schülergruppen wird eine pädagogisch ausgewogener und altersgemäßer Workshop angeboten:</p>

<ul>
	<li><b>1.</b> Sie wählen nur eine Führung (ca. 90 Minuten: Interessant, locker und informativ).</li><br>

	<li><b>2.</b> Sie dehnen den Besuch auf 2 bis 3 Stunden aus. Dann nehmen die Schüler zusätzlich an einem interessanten "Experimental-Workshop" mit vielen eigenen Experimenten teil. Hier werden sie in Gruppen aktiv, arbeiten an  historischen Geräten, experimentieren und knacken knifflige Spiele (siehe unten).</li> </ul>
	
	
	
	<p>Was sind "interessierte" Schülergruppen? <br>
	<ul>
	<li>1.	Physik-Leistungskurse der gymnasialen Oberstufe</li>
	<li>2.	Informatik-Kurse</li>
	<li>3.	Physik-Grundkurse und Mathe-Leistungskurse</li>
	<li>4.	Projektgruppen und Arbeitsgemeinschaften im naturwissenschaftlichen bzw. technischen Bereich ab der 5. Klasse</li>
	<li>5.	Gruppen aus Berufsfachschulen usw.</li>
	<li>6.	Schülergruppen aus dem Grundschulbereich, die z.B. ein Projekt (Natur und Technik) bearbeiten.</li>
	</ul>
	Wir können uns jeweils auf verschiedene Niveaus optimal einstellen!</p>

	
<p>
Wichtiger Tipp für Lehrer: Bedenken Sie, dass Schüler von dem Vorschlag ein Museum zu besuchen selten hellauf begeistert sind. Vermeiden Sie daher das Wort "Museum", denn das klingt nach "alt, staubig und langweilig". Diese Einstellung ändert sich zwar nach dem Besuch in der Regel grundlegend, dennoch ist es sinnvoller ihn z.B. einfach mit "Workshop Computer-History" zu bezeichnen. Wenn man Inhalte interessant und zeitgemäß verpackt, lassen sie sich besser vermitteln. </p>

<p>
Unser hochgestecktes Ziel ist, Schüler für Technik zu interessieren. Dies ist anhand "begreifbarer" historischer Technik als Einstieg durchaus möglich. Insbesondere im Bereich "Computer" ist eine Anbindung von neuer an alte Technik machbar und führt schnell auf sehr interessante Themen.</p>


	Auch interessierte Schüler der 4. Klasse (Grundschule) können mit Spaß und Erfolg an einem "Workshop" teilnehmen.<br>
	Gerade für Schüler dieser Altersgruppe gibt es nur wenige Möglichkeiten mit transparenter Technik in Berührung zu kommen. Hier ist das problemlos und sehr lebendig möglich!
	</p><br>
	
