	 <h2>Faxtechnik, Bildtelegraphie</h2>
	<p>Die Faksimile-Telegrafie zählt zu den ältesten Telegrafietechniken. Von der Faszination, Bilder, Zeichnungen und Schriftstücke zu versenden begeistert, wurde bereits 1843, 33 Jahre vor Einführung des Telefons, von Alexander Bain ein Vorschlag für einen "Kopiertelegraphen" gemacht. Im Jahre 1865 kam es zu der ersten praktischen Anwendung. Zwischen Paris und Lyon wurde über eine Entfernung von 400 km "fernkopiert".</p>  
    <p>Der erste in Serie hergestellte Bildtelegraph (Fultograph) kam bereits 1929 auf den Markt. Dieses Gerät wäre allerdings nur mit erheblichem Aufwand noch zu betreiben. </br>
        Die ersten praktikablen Faxgeräte waren eine deutsche Erfindung: Das Normalpapierfax von Siemens-Hell, Bj. 1956 mit Röhrentechnik, ist noch voll funktionsfähig. Gemächlich und durchschaubar dauert eine Faxübertragung (DIN A5) etwa 4 Minuten. Mit ihm könnte man theoretisch sogar mit Hilfe einiger Tricks ein Fax in Farbe übertragen!</p>

    <div class="box center">
        <img src="/shared/photos/kommunikationstechnik/fultograph.jpg" width="487" height="338" alt="Fotografie des Fultographs von Otho Fulton" />
        <div class="cols">
            <div class="leftcol">
                <p>Der <b>Fultograph</b> ist ein von dem Engländer Otto Fulton 1929 entwickelter <b>"Bildfunkempfänger"</b>. Im Gegensatz zu dem Bildtelegraphen, der über Leitungen kommunizierte, konnte der Bildfunkempfänger über Sender, die gerade erst für den Rundfunk entwickelt wurden, betrieben werden. 
                <br/>Der folgende Text wurde einer unbekannten Quelle (ca. 1930) entnommen und demonstriert eine Zeit, die für heutige Leser wie die einer fremden Welt erscheint:</p>
                <blockquote>"Wer hätte nicht schon oft beim Anhören einer packenden Rundfunkübertragung aus der Oper oder beim Genuß eines Sendespieles den Wunsch empfunden, das Dargebotene nicht nur  mit den Ohren, sondern auch mit den Augen zu erleben; wie oft hat man es bedauert, auf die eigene Einbildungskraft angewiesen zu sein und nicht auch gleichzeitig die Vorgänge sehen zu können, die in naturgetreuer Wiedergabe an des Hörers Ohr dringen ?  Die Erfüllung dieses Wunsches, die durch das Fernkino möglich wäre, scheitert nicht nur an der technischen Kompliziertheit des Problems, vielmehr ganz besonders an den enormen Kosten, die eine derartige Apparatur verursachen würde. Es ist nun eine geradezu unschätzbare Errungenschaft, daß jeder Besitzer eines guten Rundfunk-Apparates in die Lage gesetzt ist, wenigstens eine Vorstufe des Fernkinos sein Eigen zu nennen, und zwar einen Bildfunkempfänger, der ihm schöne und haltbare Bilder aus dem Äther fängt. Die Rundfunk-Sendestation kann künftig ihre akustischen Darbietungen illustrieren, kann Szenenbilder von Aufführungen, Porträts von Künstlern, Erläuterungen zu Vorträgen, Illustrationen zu den Tagesberichten, Skizzen sportlicher Ereignisse, Wetterkarten, Preisrätsel u.a.m.</blockquote>
            </div>
            <div class="rightcol">
                <blockquote>auf einfache Weise zum Gegenstand der Rundfunksendungen machen, und alle Besitzer des geeigneten Apparates können ohne technische Vorkenntnisse, ohne Dunkelkammer diese Bilder empfangen, die in wenigen Minuten in leuchtend brauner Farbe vor ihren Augen entstehen und sofort in dauerhafter Form fertig sind.  Die einfache Apparatur, die diese Wunder vermittelt, wurde in aller Stille von einem englischen Erfinder, dem Kapitän Otho Fulton, zu dieser Vollkommenheit durchgebildet, und man wird in kurzem schon in den meisten europäischen Ländern regelmäßige Bildsendungen durchführen, so daß der Besitzer des "Fultograph" - so nennt Fulton seinen Bildempfänger - ein internationales Bildsendeprogramm bei sich zu Hause aufnehmen kann.  Die Bilder sind originalgetreu, deutlich und gefällig, außerdem durch ihre Körnung von besonderer künstlerischer Wirkung. Die Übertragungszeit für ein Bild im Format 9x12 Zentimeter beträgt 3-5 Minuten."</blockquote>
                <p>Zu einer Vorstufe des Fernkinos entwickelte sich der Fultograph nicht, aber immerhin wurde die Vorhersage wahr, dass Wetterkarten per Funk gesendet und empfangen werden konnten.
                <br /> Die mit einem chemisch präparierten Papier überzogene,  sich drehende Walze wurde spiralförmig durch einen aufgesetzten "Schreibstift" (Tabulator) abgetastet. So konnte ein elektrischer Strom vom Stift über das Papier zur Walze im Rhythmus der Bildinformation fließen. Das Bild entstand damit quasi per Elektrolyse.</p>
