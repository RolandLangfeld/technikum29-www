	<p>
	    Dieser Rechner wurde ab 1952 gebaut. Man bezeichnete damals solche Rechner auch als "elektronisches Rechengerät" (original BULL-Übersetzung im technischen Handbuch) oder "Elektronenrechner" bzw. in der damaligen Hochachtung vor solchen Leistungen auch als "Elektronengehirn".
	    <br/>Der Rechner mit einem unscheinbaren Äußerem war an Lochkartenmaschinen anschließbar, so z.B. an die Tabelliermaschine BS, den Kartendoppler PRD oder den Stanzer ULP.
		<br/>Der damalige Direktor des Unternehmens "BULL", Pierre Letort beschrieb in der Zeitschrift "Arts et Manufactures", Nr. 22 vom Juni 1953, die Arbeitsweise des Gamma 3:
	</p>
	
	<blockquote> <!-- blockquote = Blockzitat, homepageweit eindeutige Formatierung -->
	    "ln seiner augenblicklichen Form arbeitet der Elektronenrechner als Zusatzgerät zu den Lochkartenmaschinen, an die er angeschlossen wird. Die Lochkartenmaschine fühlt die im Zuführmagazin eingelegten Karten ab und sendet die abgefühlten Werte an den Elektronenrechner, von dem sie umgekehrt die Ergebnisse erhält, die sie je nach ihrer Arbeitsweise schreibt oder locht. Aber welche Rechenaufgabe auch gestellt sein mag, das Ergebnis des Elektronenrechners entsteht so schnell, daß es zeitlos zu sein scheint."
	</blockquote>

	<p>
	    Unsere GAMMA 3 ist voll ausgebaut und verfügt über 7 Laufzeitspeicher mit einer Kapazität von je 12 Dezimalstellen (siehe "Speichermedien") die sich in einen Rechenspeicher und 6 Zahlenspeicher aufteilen. Zur Speichererweiterung gab es noch so genannte "Speicherschränke" mit 24 Speichereinheiten für je 12 Dezimalstellen, also der Platzbedarf eines Kleiderschrankes zum Abspeichern von 24 zwölfstelligen Dezimalzahlen!!! <br/>Insgesamt ist der Rechner mit knapp 400 Elektronenröhren bestückt. Die Anpassung des schnellen Elektronenrechners an die langsame Lochkartenmaschine erfolgt durch Thyratrons. Eine Thyratronröhre arbeitet ähnlich wie ein Thyristor (Halbleiterbauelement) und kann binäre Zustände zwischenspeichern.
	</p>
	
	<div class="box center auto-bildbreite">
		<img src="/shared/photos/rechnertechnik/bull-gamma3.jpg" alt="BULL GAMMA 3 Innen" width="692" height="565" />
		<p class="bildtext"><b>Röhren, Röhren, Röhren...BULL Gamma 3</b></div>
		
		<p>Die einzelnen Module können ausgeklappt werden, wodurch das Gerät sehr servicefreundlich ist. Zwei große Ventilatoren ziehen die durch die Heizung der Röhren bedingte Wärme aus dem Gehäuse. Links sieht man einen Teil des gigantischen Netzteils. 39 Sicherungen schützen die Stromkreise.
            <br/>Beim Anschluss an unsere BULL Tabelliermaschine werden die Informationen der Lochkartenabfühlbürsten direkt in den Gamma 3 eingelesen. Es lassen sich so Programme und Daten eingeben, die vom Gamma 3 unmittelbar bearbeitet werden. Die Ausgabe der Ergebnisse wird in das Druckwerk der Tabelliermaschine zurückgeführt.
		    <br/>Wir versuchen, diese Anlage in Betrieb zu nehmen, obwohl das ein schwieriges Unterfangen mit nur geringen Erfolgsaussichten ist. Die Architektur dieser Röhrenrechner der 1. Generation hat nichts mit der von Computern der letzten 30 Jahre gemeinsam. 
		    <br/>Techniker und Ingenieure, die damit gearbeitet haben und sich in dieser völlig anderen Welt des Rechnens auskannten, sind heute schon deutlich über 70 Jahre alt und stehen daher zur "Einstiegshilfe" leider kaum zur Verfügung.
		</p>
	
	
	<div class="box left clear-after">
		<img src="/shared/photos/rechnertechnik/gamma-3-netzteil.jpg" alt="BULL GAMMA 3 Netzteil" width="350" height="527" />
		<p class="bildtext">
		    Nebenstehendes Bild gibt Einblick in das gigantische Netzteil des Rechners. Jetzt kann man sich vorstellen, wie die Netzteile von sehr großen Röhrenrechnern aussehen müssen!<br>Um Wechselstrom in Gleichstrom umzuwandeln, verwendete man "Selen-Gleichrichter". Selen-Gleichrichter bestehen aus Metallscheiben, die auf einer Seite mit dem Selen-Halbleitermaterial beschichtet sind. Der Anschluss erfolgt durch Kontaktflächen, die mechanisch an der Scheibe anliegen. Die Sperrspannung je Scheibe beträgt etwa 25 bis 30 Volt. Um höhere Sperrspannungen zu erreichen, musste man mehrere Scheiben in Reihe schalten, die meist einfach übereinander gestapelt wurden (rechts unten im Bild). Links sind ganze "Batterien" von Elektrolytkondensatoren zu erkennen.
		</p>
