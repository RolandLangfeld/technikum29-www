<h2>Didaktische Konzeption, Informationen für Lehrer</h2>

	<p>
		Prinzipiell bieten wir Führungen mit den Themenbereichen "<b>Computer History</b>" oder "<b>Kommunikationstechnik</b>" an. Diese eignen sich für technisch interessierte Gruppen und Menschen, die einfach nur neugierig sind und sich in der Kulturgeschichte der intelligentesten und wichtigsten Werkzeuge, wie Computer und Kommunikationstechniken, weiter bilden möchten. Sie müssen sich zwischen diesen Themen entscheiden, da beide Gebiete in einer Führung mental überfordern würden und zeitlich nicht realisierbar sind.
   <br><br>

		
		Für interessierte Schülergruppen (Mindestalter ca. 14 Jahre) wird eine pädagogisch ausgewogene und altersgemäße Führung angeboten:</p>

<ol>
	<li>Sie wählen nur die Führung (ca. 90 Minuten: Interessant, locker und informativ).</li><br>

	<li>Sie dehnen den Besuch auf 2 bis 3 Stunden aus. Dann nehmen die Schüler zusätzlich an einer interessanten "Rallye" mit vielen eigenen Experimenten teil. Hier werden sie in Gruppen aktiv, arbeiten an  historischen Geräten, experimentieren und knacken knifflige Spiele.</li>
</ol>

	
<p>
Wichtiger Tipp für Lehrer: Bedenken Sie, dass Schüler von dem Vorschlag ein Museum zu besuchen selten hellauf begeistert sind. Das klingt nach "alt, staubig und langweilig". Diese Einstellung ändert sich nach dem Besuch in der Regel grundlegend. Bieten Sie also einen solchen Besuch nicht alternativ zu Bowling, Kino, Grillparty o.ä. an. Binden Sie ihn besser irgendwie kurz in den Unterricht ein. Für Kurse in Informatik (hier sollte ein solcher Besuch Pflicht sein!), Physik und Mathematik gibt es genügend Anknüpfungspunkte. <br>Ein Problem stellt eventuell die Gruppengröße dar. Selbst in großen öffentlich geführten Museen sind Gruppen von deutlich mehr als 20 Personen problematisch. Das gilt natürlich erst recht hier. Ein Besuch von größeren Gruppen lässt sich nur nach Rücksprache verwirklichen.</p>
